public class Q03 {
    //Write a java program to add, subtract, multiply and divide 2 numbers.
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        int sum = a + b;
        int sub = a - b;
        int mul = a * b;
        int div = a / b;
        int mod = a % b;

        System.out.println("Sum: " + sum + "\nSubtract: " + sub + "\nMultiply: " + mul + "\nDivide: " + div + "\nModulo: " + mod);
    }   
}