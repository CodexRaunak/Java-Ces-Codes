class SimpleIf
{
	public static void main(String vars[])
	{
		int num=Integer.parseInt(vars[0]);
		//if statement
		if(num%5==0)
		{
			System.out.println("The Number "+num+" is divisible by 5");
		}//if close
		else 
		{
			System.out.println("The Number "+num+" is Not divisible by 5");
		}// else close
		System.out.println("This program is written and executed by Prof. N. K. Nrip(2K23F023");
	
	}//main Close
}//Class close

 